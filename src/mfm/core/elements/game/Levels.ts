export const loadLevel = (tile: Tile, levelData: any) => {
  levelData.atoms.forEach((a: any) => {
    if (ElementTypes.TYPES_MAP.get(a.element)) {
      if (a.params) {
        tile.getSiteByCoord(a.gridPos).atom = ElementTypes.TYPES_MAP.get(a.element)?.class.CREATE(a.params);
      } else {
        tile.getSiteByCoord(a.gridPos).atom = ElementTypes.TYPES_MAP.get(a.element)?.class.CREATE();
      }
    }
  });
};

